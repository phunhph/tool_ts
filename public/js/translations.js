function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    localStorage.setItem('lang', lang);

    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' && el.getAttribute('placeholder')) {
                el.placeholder = translations[lang][key];
            } else if (el.tagName === 'IMG' && el.getAttribute('alt')) {
                el.alt = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update Language Dropdown Text locally if needed, but the UI has "Language" static usually.
    // Maybe update the current selected item active state?
    // For now, just updating content is enough.
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'en';
    setLanguage(savedLang);
});
